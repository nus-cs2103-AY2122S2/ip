package duke;

import java.io.IOException;

import duke.ui.MainWindow;
import duke.ui.Ui;
import duke.command.Command;
import duke.exception.DukeException;
import duke.io.Storage;
import duke.task.TaskList;
import javafx.application.Application;
import javafx.fxml.FXMLLoader;
import javafx.scene.Scene;
import javafx.scene.layout.AnchorPane;
import javafx.stage.Stage;

/**
 * Represents the Duke Application.
 *
 * @author Zheng Teck
 * @version 1.0
 */
public class Duke extends Application {

    private static TaskList taskList;
    private static Storage storage;
    private static final Parser parser = new Parser();
    private static Ui ui;

    /**
     * Initialization of Duke application.
     *
     * Loads the task list into from storage if it exists.
     */
    public Duke() {
        storage = new Storage(parser);
        try {
            taskList = storage.importTasks();
        } catch (IOException e) {
            ui.print(Ui.MSG_FILEREADERROR);
        }
    }

    /**
     * The method is used to handle the user input and provide a sutable respone.
     *
     * @param inputTxt The user input command and arguments.
     * @return The response generated by the bot after processing the command.
     */
    public String getResponse(String inputTxt) {
        try {
            Command c = parser.parse(inputTxt);
            if (c.isInvalid()) {
                return Ui.PREFIX_INVALID + c.execute(taskList, storage).trim();
            }
            return c.execute(taskList, storage).trim();
        } catch (DukeException e) {
            return Ui.PREFIX_INVALID + e.getMessage();
        } catch (IOException e) {
            return Ui.PREFIX_INVALID + Ui.MSG_FILEWRITEERROR;
        }
    }

    /**
     * The main entry point for the Duke Application.
     * The start method is called after the init method has returned, and after the system is ready for the application
     * to begin running.
     *
     * @param stage the stage for this application, onto which the application scene can be set.
     */
    @Override
    public void start(Stage stage) {
        try {
            FXMLLoader fxmlLoader = new FXMLLoader(Duke.class.getResource("/view/MainWindow.fxml"));
            AnchorPane ap = fxmlLoader.load();
            Scene scene = new Scene(ap);

            ui = new Ui(fxmlLoader.<MainWindow>getController().getDialogContainer());

            stage.setScene(scene);
            stage.setTitle("DatoDato");
            stage.setResizable(false);

            fxmlLoader.<MainWindow>getController().setDuke(this);

            ui.welcomeMsg();

            stage.show();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
}
