package helper;

/**
 * <h1>CarryOn</h1>
 * <p>
 * CarryOn contains instructions on whether to continue the program,
 * or stop taking input from the user.
 * </p>
 *
 * @author Saravanan Anuja Harish
 */
public enum CarryOn {
    // NEXT indicates that the user does not want to exit yet.
    // STOP indicates that the user wants to exit.
    USERNAME, CONTINUE_FROM_LAST_TIME, NEXT, STOP
}
